#!/usr/bin/env python3
'''
Module to concatenate two strings
'''


def concat(str1: str, str2: str) -> str:
    '''
    concatenate two strings.

    Args:
    str1 (str): string to concatenate
    str2 (str): string to concatenate

    Returns:
    str: concatenated string.
    '''

    return str1 + str2
